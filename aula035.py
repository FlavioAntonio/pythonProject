def triplicar(x):
    tri = x * 3
    return tri
def multiplicar(x):
    mult = x * 2
    return mult
def quadruplicar(x):
    quadru = x * 4
    return quadru

triplicado = triplicar(3)
multplicado = multiplicar(10)
quadruplicado = quadruplicar(15)

print(f'Valor triplicado {triplicado}')
print(f'Valor Multiplicado {multplicado}')
print(f'valor quadruplicado {quadruplicado}')